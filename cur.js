const BASE_URL ="https://cdn.jsdelivr.net/npm/@fawazahmed0/currency-api@latest/v1/currencies";
const dropdown=document.querySelectorAll(".dropdown select");
const from=document.querySelector(".from img");
const to=document.querySelector(".to img");
const btn=document.querySelector(".submit");
let fromcur=document.querySelector(".from select");
let tocur=document.querySelector(".to select");

for(let select of dropdown){
    for(let code in countryList){
        let newoption=document.createElement("option");
        newoption.innerText=code;
        newoption.value=code;
        select.append(newoption);
    }

select.addEventListener("change",()=>{
    
    let curcode=select.value;
    let concode=countryList[select.value];
    let newsrc=`https://flagsapi.com/${concode}/flat/64.png`;
    if(select.name==="from"){
        from.src=newsrc;}
    else{
        to.src=newsrc;
    }
});
}
btn.addEventListener("click",async(evn)=>{
    evn.preventDefault();
    let amount=document.querySelector(".amount input");
    if(amount.value<1){
        amount.value="1";
    }
    const url=`${BASE_URL}/${fromcur.value.toLowerCase()}.json`;
    let response=await fetch(url);
    let data=await response.json();
    let msg=document.querySelector(".msg");
    msg.innerText=`=> ${amount.value} ${fromcur.value} = ${amount.value*data[fromcur.value.toLowerCase()][tocur.value.toLowerCase()]} ${tocur.value}`
    msg.style.backgroundColor="yellow";
    msg.style.color="red";
})